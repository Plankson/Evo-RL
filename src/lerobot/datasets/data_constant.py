def resolve_dataset_repo_id(repo_id: str | list[str]) -> str | list[str]:
    resolved: Any = globals().get(repo_id)
    if isinstance(resolved, list) and all(isinstance(item, str) for item in resolved):
        return list(resolved)
    return repo_id
